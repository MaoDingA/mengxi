/// Very simple bitmap text (3x5 pixel font for ASCII).
///
/// Draws text as light-gray pixels (200, 200, 200) into an RGB8 image buffer.
/// Each character is 3 pixels wide with 1 pixel spacing (4px stride).
pub(crate) fn draw_text_simple(
    img: &mut [u8],
    w: usize,
    h: usize,
    x0: usize,
    y0: usize,
    text: &str,
) {
    let font: Vec<(char, Vec<u8>)> = vec![
        // Uppercase A-Z
        ('A', vec![0b111, 0b101, 0b111, 0b101, 0b101]),
        ('B', vec![0b110, 0b101, 0b110, 0b101, 0b110]),
        ('C', vec![0b111, 0b100, 0b100, 0b100, 0b111]),
        ('D', vec![0b110, 0b101, 0b101, 0b101, 0b110]),
        ('E', vec![0b111, 0b100, 0b111, 0b100, 0b111]),
        ('F', vec![0b111, 0b100, 0b111, 0b100, 0b100]),
        ('G', vec![0b111, 0b100, 0b101, 0b101, 0b111]),
        ('H', vec![0b101, 0b101, 0b111, 0b101, 0b101]),
        ('I', vec![0b111, 0b010, 0b010, 0b010, 0b111]),
        ('J', vec![0b001, 0b001, 0b001, 0b101, 0b111]),
        ('K', vec![0b101, 0b110, 0b100, 0b110, 0b101]),
        ('L', vec![0b100, 0b100, 0b100, 0b100, 0b111]),
        ('M', vec![0b101, 0b111, 0b101, 0b101, 0b101]),
        ('N', vec![0b101, 0b111, 0b111, 0b101, 0b101]),
        ('O', vec![0b111, 0b101, 0b101, 0b101, 0b111]),
        ('P', vec![0b111, 0b101, 0b111, 0b100, 0b100]),
        ('Q', vec![0b111, 0b101, 0b101, 0b111, 0b001]),
        ('R', vec![0b111, 0b101, 0b111, 0b110, 0b101]),
        ('S', vec![0b111, 0b100, 0b111, 0b001, 0b111]),
        ('T', vec![0b111, 0b010, 0b010, 0b010, 0b010]),
        ('U', vec![0b101, 0b101, 0b101, 0b101, 0b111]),
        ('V', vec![0b101, 0b101, 0b010, 0b101, 0b101]),
        ('W', vec![0b101, 0b101, 0b111, 0b111, 0b101]),
        ('X', vec![0b101, 0b111, 0b000, 0b111, 0b101]),
        ('Y', vec![0b101, 0b101, 0b010, 0b010, 0b010]),
        ('Z', vec![0b111, 0b001, 0b010, 0b100, 0b111]),
        // Lowercase a-z (where meaningful in 3x5)
        ('a', vec![0b000, 0b010, 0b101, 0b111, 0b101]),
        ('b', vec![0b100, 0b100, 0b110, 0b101, 0b110]),
        ('c', vec![0b000, 0b010, 0b100, 0b100, 0b010]),
        ('d', vec![0b001, 0b001, 0b011, 0b101, 0b011]),
        ('e', vec![0b000, 0b010, 0b101, 0b110, 0b101]),
        ('f', vec![0b010, 0b101, 0b110, 0b100, 0b100]),
        ('g', vec![0b000, 0b111, 0b101, 0b011, 0b111]),
        ('h', vec![0b100, 0b100, 0b110, 0b101, 0b101]),
        ('i', vec![0b000, 0b010, 0b110, 0b010, 0b010]),
        ('j', vec![0b000, 0b001, 0b011, 0b001, 0b110]),
        ('k', vec![0b100, 0b100, 0b110, 0b101, 0b101]),
        ('l', vec![0b000, 0b110, 0b010, 0b010, 0b111]),
        ('m', vec![0b000, 0b000, 0b101, 0b111, 0b101]),
        ('n', vec![0b000, 0b000, 0b110, 0b101, 0b101]),
        ('o', vec![0b000, 0b000, 0b010, 0b101, 0b010]),
        ('p', vec![0b000, 0b111, 0b101, 0b111, 0b100]),
        ('q', vec![0b000, 0b011, 0b101, 0b111, 0b001]),
        ('r', vec![0b000, 0b110, 0b101, 0b110, 0b100]),
        ('s', vec![0b000, 0b010, 0b100, 0b001, 0b010]),
        ('t', vec![0b000, 0b000, 0b110, 0b010, 0b010]),
        ('u', vec![0b000, 0b000, 0b101, 0b101, 0b111]),
        ('v', vec![0b000, 0b000, 0b101, 0b101, 0b010]),
        ('w', vec![0b000, 0b000, 0b101, 0b111, 0b101]),
        ('x', vec![0b000, 0b000, 0b101, 0b010, 0b101]),
        ('y', vec![0b000, 0b101, 0b101, 0b011, 0b101]),
        ('z', vec![0b000, 0b000, 0b111, 0b010, 0b111]),
        // Digits 0-9
        ('0', vec![0b111, 0b101, 0b101, 0b101, 0b111]),
        ('1', vec![0b010, 0b110, 0b010, 0b010, 0b111]),
        ('2', vec![0b111, 0b001, 0b111, 0b100, 0b111]),
        ('3', vec![0b111, 0b001, 0b111, 0b001, 0b111]),
        ('4', vec![0b101, 0b101, 0b111, 0b001, 0b001]),
        ('5', vec![0b111, 0b100, 0b111, 0b001, 0b111]),
        ('6', vec![0b111, 0b100, 0b111, 0b101, 0b111]),
        ('7', vec![0b111, 0b001, 0b010, 0b010, 0b010]),
        ('8', vec![0b111, 0b101, 0b111, 0b101, 0b111]),
        ('9', vec![0b111, 0b101, 0b111, 0b001, 0b111]),
        // Symbols
        ('.', vec![0b000, 0b000, 0b000, 0b000, 0b010]),
        ('%', vec![0b100, 0b001, 0b010, 0b100, 0b001]),
        (' ', vec![0b000, 0b000, 0b000, 0b000, 0b000]),
        ('-', vec![0b000, 0b000, 0b111, 0b000, 0b000]),
    ];

    let mut cx = x0;
    for ch in text.chars() {
        if let Some(entry) = font.iter().find(|(c, _)| *c == ch) {
            for (row, bits) in entry.1.iter().enumerate() {
                for col in 0..3usize {
                    if bits & (1 << (2 - col)) != 0 {
                        let px = cx + col;
                        let py = y0 + row;
                        if px < w && py < h {
                            let idx = (py * w + px) * 3;
                            if idx + 2 < img.len() {
                                img[idx] = 200;
                                img[idx + 1] = 200;
                                img[idx + 2] = 200;
                            }
                        }
                    }
                }
            }
        }
        cx += 4;
    }
}
